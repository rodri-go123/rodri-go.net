function closeAbout() {
  document.getElementById("about").style.display = "none";
}

function openAbout() {
  document.getElementById("about").style.display = "block";
  document.getElementById("index").style.display = "none";
}

function closeIndex() {
  document.getElementById("index").style.display = "none";
}

function openIndex() {
  document.getElementById("index").style.display = "block";
  document.getElementById("about").style.display = "none";
}
